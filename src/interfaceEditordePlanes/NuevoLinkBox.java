    private void accionesOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionesOrigenActionPerformed
        // TODO add your handling code here:
        JComboBox cb = (JComboBox)evt.getSource();
        a_origen= (Accion)cb.getSelectedItem();
        int nivel_origen= 0;
        int niveles= plan.cantidadNivelesAcciones();
        accionesDestino.removeAllItems();
        if(a_origen!=null){
            for(int i=nivel_origen; i <= niveles; i++){
                Vector<Accion> acciones_nivel= plan.obtenerAccionesDeNivel(new Integer(i));
                if(acciones_nivel != null &&  i > a_origen.obtenerNivel())
                   for(Enumeration<Accion> e= acciones_nivel.elements(); e.hasMoreElements();)
                       accionesDestino.addItem(e.nextElement());
            }
            if(plan.obtenerEstFinal() != null)
                accionesDestino.addItem(plan.obtenerEstFinal());
            if(accionesDestino.getItemCount() > 0){
                accionesDestino.setEnabled(true);
            }
        }
    }//GEN-LAST:event_accionesOrigenActionPerformed

    @Action
    public void aceptar(){
        IntefaceEditordePlanes.getApplication().getMainFrame().setEnabled(true);
        dispose();
    }
    @Action
    public void insertar() throws CloneNotSupportedException{
        Link link_nuevo= new Link(a_origen,a_destino,pre_comun);
        if(plan.agregarLink(link_nuevo)){
            itv.reDibujar();
            //---
            itv.setPlanClonado((Plan) plan.clone());
            itv.getOriginador().set(itv.getPlanClonado());
            itv.getPortero().addMemento( itv.getOriginador().salvarEnRecuerdo());
            //---
        }
    }
        
