    private void accionesDestinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionesDestinoActionPerformed
        // TODO add your handling code here:    
        JComboBox cb = (JComboBox)evt.getSource();
        a_destino= (Accion)cb.getSelectedItem();
        if(a_destino != null && a_origen != null  && a_origen.obtenerNivel() < a_destino.obtenerNivel()){
            predsComun.removeAllItems();
            Vector<Predicado> preds= plan.predicosEnComun(a_origen, a_destino);
            if(preds != null)
                for(Enumeration<Predicado> e= preds.elements(); e.hasMoreElements();)
                    predsComun.addItem(e.nextElement());
            if(predsComun.getItemCount() > 0){
                predsComun.setEnabled(true);
                aceptar.setEnabled(true);
            }
            else{
                predsComun.setEnabled(false);
                aceptar.setEnabled(false);
            }
        }
}//GEN-LAST:event_accionesDestinoActionPerformed

